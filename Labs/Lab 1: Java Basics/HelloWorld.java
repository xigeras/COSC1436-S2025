public class HelloWorld // establishes the class: HelloWorld. Necessary for a Java program.
{
    public static void main(String[] args) // the main entry point for execution with our code below.
    {
        System.out.println("Hello, world!"); // this code produces the output "Hello, world!"
        System.out.println("My name is August Backman,"); // this code produces the output that is my name.
        System.out.println("and my favorite food is menudo!"); // this code produces the output of my favorite food!
        System.out.println("A famous public figure I would like to meet is Tom Cruise."); // this code produces the output of which celebrity i would like to meet.
        System.out.println("My least favorite chore at home is sweeping."); // this code produces what i dislike as a chore at home.
        System.out.println("I would rather be playing Persona 5 Royal!"); // this code produces what i would prefer to be doing instead of coding.
    }
}

