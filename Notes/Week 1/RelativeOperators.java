package Notes;
public class RelativeOperators
{
    public static void main(String[] args)
    {
        
        System.out.println(6==6); // double equal sign means equals; a single = means assignment
        System.out.println(6!=6); // the ! means "NOT"
        System.out.println(6>6); // the >/< asks if its larger on the bigger side
        System.out.println(6<6); 
        System.out.println(6<=6); // compounding the inequality with an equal sign creates an or structure
        System.out.println(6>=6);
    }
}